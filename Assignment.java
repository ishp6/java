public class Assignment 
{
    public static void main(String[] args) {
        int A = 10;
        int B = 5;
        System.out.println(A);
        System.out.println(B);
        A += 10; // A = A + 10
        System.out.println(A);
        B -= 5;  // B = B - 5
        System.out.println(B);
        A *= 2;  // A = A * 2
        System.out.println(A);
        B /= 5;  // B = B / 5
        System.out.println(B);
        A %= 5;  // A = A % 5
        System.out.println(A);
        
    }
}